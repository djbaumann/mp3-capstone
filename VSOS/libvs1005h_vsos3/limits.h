#define INT_MIN   -32768      /* minimum value of int */
#define LONG_MAX  2147483647  /* maximum value of long */
#define LONG_MIN  -2147483648 /* minimum value of long */
#define SCHAR_MAX 127         /* maximum value of signed char */
#define SCHAR_MIN -128        /* minimum value of signed char */
#define SHRT_MAX  32767       /* maximum value of short */
#define SHRT_MIN  -32768      /* minimum value of short */
#define UINT_MAX  65535U      /* maximum value of unsigned int */
#define ULONG_MAX 4294967295UL /* maximum value of unsigned long */
#define USHRT_MAX 65535U      /* maximum value of unsigned short */

#endif /* _LIMITS_H_ */
